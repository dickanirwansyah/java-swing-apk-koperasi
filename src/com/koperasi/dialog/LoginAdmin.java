    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // proses login
       adminDAO=new AdminDAOImpl();
       String username=txtUsername.getText();
       char[] pwd=txtPassword.getPassword();
       String password=new String(pwd);
       
       if(username.trim().isEmpty()){
           JOptionPane.showMessageDialog(null, "username masih kosong");
       }else if(password.trim().isEmpty()){
           JOptionPane.showMessageDialog(null, "password masih kosong");
       }else{
           Admin loginAdmin=adminDAO.getLoginAdmin(username, password);
           if(loginAdmin!=null){
               a=loginAdmin;
               Master m=new Master();
               m.setVisible(true);
               m.setAdmin(a);
               dispose();
           }else{
               JOptionPane.showMessageDialog(null, "maaf username dan password tidak valid !");
           }
       }
    }//GEN-LAST:event_btnLoginActionPerformed

